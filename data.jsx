// ── Live name upgrade ────────────────────────────────────────────────────────
// App.jsx registers a callback into window.APP_RERENDER so we can trigger a
// React re-render once the names map arrives.
function applyProductNames(map) {
  if (!map || typeof map !== 'object') return 0;
  // Merge into the existing map so a partial API response doesn't wipe out
  // names already supplied by the static embed.
  PRODUCT_NAMES = { ...PRODUCT_NAMES, ...map };
  let hits = 0;
  for (const p of ALL_PRODUCTS) {
    const n = PRODUCT_NAMES[p.sku];
    if (n && typeof n === 'string' && p.name !== n) {
      p.name = n;
      hits++;
    }
  }
  if (typeof window !== 'undefined' && typeof window.APP_RERENDER === 'function') {
    window.APP_RERENDER();
  }
  return hits;
}

async function fetchProductNames() {
  try {
    const ctrl = new AbortController();
    const t = setTimeout(() => ctrl.abort(), 12000);
    const res = await fetch(`${API_BASE}/product-names`, { signal: ctrl.signal });
    clearTimeout(t);
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();
    // The API wraps the map: { total: N, names: { sku: name, ... } }.
    // Accept either shape so future versions that return the flat map still work.
    const map = data && typeof data === 'object' && data.names && typeof data.names === 'object'
      ? data.names
      : data;
    const hits = applyProductNames(map);
    console.info(`[supplypredict] product-names upgraded ${hits}/${ALL_PRODUCTS.length} SKUs`);
    return hits;
  } catch (e) {
    console.warn('[supplypredict] /product-names fetch failed, falling back to static name map', e);
    return 0;
  }
}

